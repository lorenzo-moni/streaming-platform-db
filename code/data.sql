CREATE PROCEDURE generaErogazioni()
BEGIN
    -- Loggare tutti gli utenti che abbiamo in posti diversi
    -- Per ogni utente generare un erogazione in corso e una terminata


    DECLARE indice INT DEFAULT 1;
    DECLARE finito_ INT DEFAULT 0;
    DECLARE idUtente_ INT UNSIGNED;
    DECLARE idUtenteN_ INT UNSIGNED;
    DECLARE emailUtente_ VARCHAR(255);
    DECLARE passwordUtente_ VARCHAR(255);
    DECLARE ipAddress_ VARCHAR(255);
    DECLARE versione_ VARCHAR(10);
    DECLARE sistemaOperativo_ VARCHAR(255);
    DECLARE macAddress_ VARCHAR(255);
    DECLARE nome_ VARCHAR(255);
    DECLARE tipo_ VARCHAR(255);
    DECLARE latitudine_ VARCHAR(255);
    DECLARE longitudine_ VARCHAR(255);
    DECLARE durataFilm_ SMALLINT UNSIGNED;
    DECLARE idContenuto1_ INT UNSIGNED;
    DECLARE idFilm1_ INT UNSIGNED;
    DECLARE idContenuto2_ INT UNSIGNED;
    DECLARE minutiVisti1_ SMALLINT UNSIGNED;


    DECLARE cursoreUtenti CURSOR FOR
        SELECT U.id, U.mail, U.password
        FROM Utente U
        WHERE NOT EXISTS(SELECT 1 FROM Connessione C1 WHERE C1.idUtente = U.id AND C1.fine IS NULL);

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET finito_ = 1;

    OPEN cursoreUtenti;

    scan :
    LOOP

        FETCH cursoreUtenti INTO idUtente_, emailUtente_, passwordUtente_;

        IF finito_ = 1 THEN
            LEAVE scan;
        END IF;

        WITH dispositivi AS (SELECT D.*, ROW_NUMBER() OVER () AS numero
                             FROM Dispositivo D)
        SELECT ipAddress, nome, sistemaOperativo, versione, macAddress, tipo
        INTO ipAddress_, nome_, sistemaOperativo_, versione_, macAddress_, tipo_
        FROM dispositivi D1
        WHERE D1.numero = idUtente_;

        SET latitudine_ = indice * 3 % 90;
        SET longitudine_ = indice * 5 % 180;

        WITH ContenutiNonRistretti AS (SELECT C.*, ROW_NUMBER() OVER () AS numero
                                       FROM Contenuto C
                                       WHERE NOT EXISTS(SELECT 1
                                                        FROM RestrizioneContenuto RC
                                                        WHERE RC.idContenuto = C.id)
                                         AND C.risoluzione IN ('FHD', 'HD'))
        SELECT CNR.id
        INTO idContenuto1_
        FROM ContenutiNonRistretti CNR
        WHERE CNR.numero = ((indice * 3) % (SELECT COUNT(*) FROM ContenutiNonRistretti));

        WITH ContenutiNonRistretti AS (SELECT C.*, ROW_NUMBER() OVER () AS numero
                                       FROM Contenuto C
                                       WHERE NOT EXISTS(SELECT 1
                                                        FROM RestrizioneContenuto RC
                                                        WHERE RC.idContenuto = C.id)
                                         AND risoluzione IN ('FHD', 'HD'))
        SELECT CNR.id
        INTO idContenuto2_
        FROM ContenutiNonRistretti CNR
        WHERE CNR.numero = ((indice * 7) % (SELECT COUNT(*) FROM ContenutiNonRistretti));


        CALL loginUtente(emailUtente_, passwordUtente_, ipAddress_, versione_, sistemaOperativo_, nome_, macAddress_,
                         tipo_, latitudine_, longitudine_, idUtenteN_);


        CALL VisualizzazioneContenuto(idUtente_, idContenuto1_);

        SELECT durata, F.id
        INTO durataFilm_, idFilm1_
        FROM Film F
                 INNER JOIN Contenuto C ON C.idFilm = F.id
        WHERE C.id = idContenuto1_;

        SET minutiVisti1_ = ((durataFilm_ * 0.7 + indice * 3) % durataFilm_);


        UPDATE Erogazione E
        SET E.fine      = NOW(),
            minutiVisti = minutiVisti1_
        WHERE idUtente = idUtente_
          AND fine IS NULL;

        IF (minutiVisti1_ / durataFilm_ > 0.25) THEN
            INSERT INTO RecensioneUtente(idUtente, idFilm, data, votazione)
            VALUES (idUtente_, idFilm1_, CURRENT_DATE(), indice % 2);
        END IF;

        CALL VisualizzazioneContenuto(idUtente_, idContenuto2_);


        SET indice = indice + 1;

        SET finito_ = 0;

    END LOOP scan;

    CLOSE cursoreUtenti;


END $$

